CREATE DATABASE cookmate1_db;

USE cookmate1_db;

-- Ingredient Table
CREATE TABLE Ingredient (
    ingredient_id VARCHAR(10) PRIMARY KEY,
    ingredient_name VARCHAR(255),
    Categories_name VARCHAR(255),
    Categories_image VARCHAR(255),
    exp_date DATE,
    description TEXT
);

CREATE TABLE Recipes (
    recipe_id VARCHAR(10) PRIMARY KEY,
    recipe_name VARCHAR(255),
    instruction TEXT,
    recipe_image VARCHAR(255),
    fav_id BOOLEAN DEFAULT FALSE
);

CREATE TABLE Recipes_Ingredients (
    recipes_ingredients_id VARCHAR(10) PRIMARY KEY,
    recipe_id VARCHAR(10),
    ingredient_id VARCHAR(10),  -- ขนาดตรงกับ ingredient_id ใน Ingredient
    is_required BOOLEAN,
    FOREIGN KEY (recipe_id) REFERENCES Recipes(recipe_id),  -- ดึง PK จาก Recipes
    FOREIGN KEY (ingredient_id) REFERENCES Ingredient(ingredient_id)  -- ดึง PK จาก Ingredient
);



